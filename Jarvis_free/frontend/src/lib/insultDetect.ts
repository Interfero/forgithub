/** Грубые оскорбления (подсказка UI; счётчик — POST /api/insult/evaluate). */
const INSULT_RE =
  /\b(дурак|идиот|идиотк\w*|тупой|тупая|тупое|тупиц\w*|чучело|дебил|дебилк\w*|кретин|урод|мусор|отстой|говн\w*|дерьм\w*|сука|сучк\w*|бляд\w*|блять|блядь|хуй|хуе\w*|пизд\w*|ебан\w*|ёбан\w*|ёб\w*|еб\w*|ублюд\w*|придур\w*|тварь|скотин\w*|ничтожеств\w*|осёл|осел|козёл|козел|мудак|мудил\w*|болван|мразь)\b/i

const PHRASE_RE =
  /(иди\s*на|пошёл\s*на|пошел\s*на|заткнись|заткни\s*рот|ты\s+бот\s+для\s+идиот|хуесос|пидор|пидар|мразь|пошёл\s*вон|пошел\s*вон)/i

export function messageLooksLikeInsult(text: string): boolean {
  const t = (text || '').trim()
  if (t.length < 2) return false
  return INSULT_RE.test(t) || PHRASE_RE.test(t)
}
